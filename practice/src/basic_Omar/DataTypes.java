package basic_Omar;

public class DataTypes {
	 public static void main(String args[]){
			byte byte_val = 20;
			System.out.println("Byte value is :" +byte_val);

			short short_val = 30000;
			System.out.println("short value is :" + short_val);

			char ch ='A';
			System.out.println("char value is :" + ch);

			int int_val = 400000000;
			System.out.println("integer value is:" + int_val);

			long long_val = 500000000;
			System.out.println("long value is :" + long_val);

			float float_val = 6.777f;
			System.out.println("float value is :" + float_val);

			double dou_val = 76.777777777d;
			System.out.println("double value is :" + dou_val);

			boolean isTrue = false;
			System.out.println("boolean value is :" + isTrue);
			}
		}

