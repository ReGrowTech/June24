package Capsule_17_Sweety;

public class Even_no_btw_30_to_20
{
    public static void main(String[] args) {

        System.out.println(" Wap the even number between 30 to 20");


        for(int i=20;i<=30;i++)//20 ,<30 yes , 21,<30
        {

            if(i%2==0)//20%2=0 ,21%2=1
            {
                System.out.println(i); //20

            }
        }
    }
}
