package java_Practice;

public class Num_operation {

	public static void main(String[] args) {
		
		int num1=25,num2=5;
		int sum=0,diff=0,prod=1;
		double div=0.0D;
		sum=num1+num2;
		diff=num1-num2;
		prod=num1*num2;
		div=num1/num2;
		
		System.out.println("Sum of 2 no :"+sum);
		System.out.println("diff of 2 no "+diff);
		System.out.println("product of 2 no :"+prod);
		System.out.println("division of 2 no :"+div);

	}

}
