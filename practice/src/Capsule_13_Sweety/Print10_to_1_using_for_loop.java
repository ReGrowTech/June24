package Capsule_13_Sweety;

public class Print10_to_1_using_for_loop

  {
      public static void main(String[] args)
      {
          System.out.println(" WAP to print 10 to 1 using while loop ");

          for (int i=10 ; i>=1 ; i--)
          {

              System.out.println(i);
          }
      }
  }
