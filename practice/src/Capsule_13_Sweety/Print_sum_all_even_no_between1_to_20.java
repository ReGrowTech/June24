package Capsule_13_Sweety;

public class Print_sum_all_even_no_between1_to_20
  {
      public static void main(String[] args)
      {
          System.out.println(" WAP to print the sum of all the even no between 1 to 20 ");
           int sum = 0;

          for (int i=2 ; i<=20 ; i += 2)
          {

              sum += i ;


          }
          System.out.println(" sum of all the even no  "+sum);


      }
  }
