package unit_test_04_shad;

public class Table_of_2 {

	public static void main(String[] args) {
		
// Q11. WAP to print table of 2. Eg:- 2 X 1 = 2............ 2 x 10 = 20. 
		
		int n = 2;
		int i = 1;
		
		while(i <= 10)
		{
			System.out.println(n + " X " + i + " = " + (n * i));
			i++;
		}

	}

}
