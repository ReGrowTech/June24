// first  10 natural number
public class First10_natural_no_do_While
{
    public static void main(String[] args) {

        System.out.println("First 10 nature number : ");
        int i=1;

        do {

            System.out.println(i);
            i++;
        }
        while (i <= 10);
    }
}
