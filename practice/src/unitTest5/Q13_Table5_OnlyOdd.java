package unitTest5;

public class Q13_Table5_OnlyOdd {

	public static void main(String[] args) {
		int n = 5;
		for(int i = 1;i<=10;i++)
		{
			if(i%2!=0)
			{
			System.out.println(n + "X" + i + " = " + (n*i));
		}
	}
		
		
		//whileloop
		int k = 4;
		int j = 1;
		while(j<=10)
		{
			if(j%2!=0)
			{
			System.out.println(k + "X" + j + " = " + (k*j));
			
		}
			j++;
		}
		
}
}
